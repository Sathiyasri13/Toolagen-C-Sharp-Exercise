using System;

class Program
{
    static void Main()
    {
        // Loop through numbers from 1 to 50
        for (int i = 1; i <= 50; i++)
        {
            // Check if the number is even
            if (i % 2 == 0)
            {
                // Print the even number
                Console.WriteLine(i);
            }
        }
    }
}
